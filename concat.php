<?php 

// Example 1: Concatenating two strings
$string1 = "Hello";
$string2 = "World";
$result = $string1 . " " . $string2;
echo $result; // Output: Hello World

// Example 2: Concatenating strings with variables
$name = "Mudassir";
$age = 23;
$message = "My name is " . $name . " and I am " . $age . " years old.";
echo $message; // Output: My name is JMudassir and I am 23 years old.

// Example 3: Concatenating strings within echo statement
echo "The result is: " . (10 + 5); // Output: The result is: 15

// Example 4: Combining concatenation with assignment
$greeting = "Hello";
$greeting .= " World";
echo $greeting; // Output: Hello World


?>